enum COLOR_AND_BG_OPTIONS {
    color = "color",
    backgroundColor = "backgroundColor",
    bgColor = "bgColor",
}

type ColorAndBgBase = {
    [key in COLOR_AND_BG_OPTIONS]: Color;
}

type ColorAndBgAddons = {
    opacity: number;
}

export type ColorAndBg = ColorAndBgBase & ColorAndBgAddons;

export const getColorAndBgProps = ({ backgroundColor, bgColor, color, opacity }: Partial<ColorAndBg>) => ({
    backgroundColor: backgroundColor || bgColor || "transparent",
    color: color || "black",
    opacity: opacity || 1
});